const without = function(source, itemsToRemove) {
  let newArray = source;
  newArray = newArray.filter( function( items ) {
    return !itemsToRemove.includes( items );
  });
  return newArray;
};

console.log(without([1, 2, 3], [1]));
console.log(without(["1", "2", "3"], [1, 2, "3"]));

